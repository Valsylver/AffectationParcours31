package fr.affectation.domain.choice;

import javax.persistence.Entity;
import javax.persistence.Table;


@Entity
@Table(name = "Improvement_course_choice")
public class ImprovementCourseChoice extends Choice{
	
	private String type = "ImprovementCourse";

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		return result;
	}


	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		ImprovementCourseChoice other = (ImprovementCourseChoice) obj;
		if (type == null) {
			if (other.type != null)
				return false;
		} else if (!type.equals(other.type))
			return false;
		return true;
	}


	public String getType() {
		return type;
	}


	public void setType(String type) {
		this.type = type;
	}
	
	
	
}